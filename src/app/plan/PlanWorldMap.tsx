// Equirectangular projection, clipped to lat -56..78
function projectLL(lat: number, lng: number, W: number, H: number) {
  const LAT_MAX = 78, LAT_MIN = -56;
  const x = ((lng + 180) / 360) * W;
  const y = ((LAT_MAX - lat) / (LAT_MAX - LAT_MIN)) * H;
  return { x, y };
}

const ORIGIN_LL: Record<string, [number, number]> = {
  JFK: [40.64, -73.78],
  LAX: [33.94, -118.41],
  ORD: [41.98, -87.91],
  LHR: [51.47, -0.45],
  SFO: [37.62, -122.38],
  YYZ: [43.68, -79.63],
  SIN: [1.35, 103.99],
};

export type MapDestination = {
  id: string;
  city: string;
  lat: number;
  lng: number;
  total: number;
  tier: "perfect" | "great" | "stretch" | "over";
  popularityScore: number;
};

type Props = {
  destinations: MapDestination[];
  budget: number;
  origin: string;
  activeId: string | null;
  onHover: (id: string | null) => void;
  onSelect: (id: string | null) => void;
  tone?: "cream" | "dark";
};

export default function PlanWorldMap({
  destinations,
  budget,
  origin,
  activeId,
  onHover,
  onSelect,
  tone = "cream",
}: Props) {
  const W = 960, H = 520;
  const wrapRef = useRef<HTMLDivElement>(null);
  const [dims, setDims] = useState({ w: W, h: H });

  useEffect(() => {
    if (!wrapRef.current) return;
    const ro = new ResizeObserver(entries => {
      for (const e of entries) {
        setDims({ w: e.contentRect.width, h: e.contentRect.height });
      }
    });
    ro.observe(wrapRef.current);
    return () => ro.disconnect();
  }, []);

  const originLL = ORIGIN_LL[origin] ?? [40.64, -73.78];
  const originPt = projectLL(originLL[0], originLL[1], W, H);

  const colored = destinations.map(d => ({
    ...d,
    pt: projectLL(d.lat, d.lng, W, H),
  }));

  const sorted = [...colored].sort((a, b) => {
    if (a.id === activeId) return 1;
    if (b.id === activeId) return -1;
    const order = { over: 0, stretch: 1, great: 2, perfect: 3 };
    return order[a.tier] - order[b.tier];
  });

  const bgDots  = tone === "cream" ? "rgba(10,10,10,0.13)" : "rgba(255,255,255,0.17)";
  const landDot = tone === "cream" ? "rgba(10,10,10,0.30)" : "rgba(255,255,255,0.42)";
  const textFill = tone === "cream" ? "#0A0A0A" : "#fff";

  const COLORS = {
    perfect: "#5DA89A",
    great:   "#5DA89A",
    stretch: "#C48A3A",
    over:    "#B97A7A",
  };

  return (
    <div ref={wrapRef} style={{ position: "relative", width: "100%", height: "100%" }}>
      <svg
        viewBox={`0 0 ${W} ${H}`}
        preserveAspectRatio="xMidYMid meet"
        style={{ width: "100%", height: "100%", display: "block" }}
      >
        <defs>
          <pattern id="pmBgGrid" width="6" height="6" patternUnits="userSpaceOnUse">
            <circle cx="1" cy="1" r="0.6" fill={bgDots} />
          </pattern>
          <radialGradient id="pmPulse" cx="50%" cy="50%" r="50%">
            <stop offset="0%" stopColor="#5DA89A" stopOpacity="0.45" />
            <stop offset="100%" stopColor="#5DA89A" stopOpacity="0" />
          </radialGradient>
        </defs>

        {/* Background dot grid */}
        <rect width={W} height={H} fill="url(#pmBgGrid)" />

        {/* Equator + prime meridian */}
        <line x1="0" y1={H * (78 / 134)} x2={W} y2={H * (78 / 134)} stroke={bgDots} strokeDasharray="2 6" />
        <line x1={W / 2} y1="0" x2={W / 2} y2={H} stroke={bgDots} strokeDasharray="2 6" />

        {/* Landmass dots */}
        <g>
          {LANDMASS.map(([lat, lng], i) => {
            const { x, y } = projectLL(lat, lng, W, H);
            return <circle key={i} cx={x} cy={y} r="1.4" fill={landDot} />;
          })}
        </g>

        {/* Flight arcs to reachable destinations */}
        <g opacity="0.45">
          {sorted.filter(d => d.tier !== "over").map(d => {
            const dx = Math.abs(originPt.x - d.pt.x);
            if (dx > W / 2) return null; // skip antimeridian-crossing arcs
            const midX = (originPt.x + d.pt.x) / 2;
            const midY = Math.min(originPt.y, d.pt.y) - Math.abs(originPt.x - d.pt.x) * 0.18 - 20;
            const isActive = d.id === activeId;
            return (
              <path
                key={d.id}
                d={`M ${originPt.x} ${originPt.y} Q ${midX} ${midY} ${d.pt.x} ${d.pt.y}`}
                stroke={isActive ? "#5DA89A" : "rgba(93,168,154,0.22)"}
                strokeWidth={isActive ? 1.4 : 0.6}
                fill="none"
                strokeDasharray={isActive ? "0" : "3 4"}
              />
            );
          })}
        </g>

        {/* Origin marker */}
        <g>
          <circle cx={originPt.x} cy={originPt.y} r="14" fill="url(#pmPulse)" />
          <circle cx={originPt.x} cy={originPt.y} r="4.5" fill="#0A0A0A" />
          <circle cx={originPt.x} cy={originPt.y} r="1.8" fill="#fff" />
          <text
            x={originPt.x + 9} y={originPt.y - 8}
            fontFamily="DM Mono, monospace" fontSize="9" fontWeight="700"
            fill={textFill} letterSpacing="0.12em"
          >
            {origin}
          </text>
        </g>

        {/* Destination pins */}
        <g>
          {sorted.map(d => {
            const c = COLORS[d.tier];
            const isActive = d.id === activeId;
            const r = isActive ? 8 : d.tier === "over" ? 3 : 5;
            const op = d.tier === "over" ? 0.28 : 1;
            const showLabel = isActive || (d.tier === "perfect" && d.popularityScore >= 85);
            return (
              <g
                key={d.id}
                opacity={op}
                onMouseEnter={() => onHover(d.id)}
                onMouseLeave={() => onHover(null)}
                onClick={() => onSelect(d.id)}
                style={{ cursor: "pointer" }}
              >
                {isActive && (
                  <circle cx={d.pt.x} cy={d.pt.y} r="16" fill={c} opacity="0.2">
                    <animate attributeName="r" values="12;22;12" dur="2s" repeatCount="indefinite" />
                    <animate attributeName="opacity" values="0.35;0;0.35" dur="2s" repeatCount="indefinite" />
                  </circle>
                )}
                <circle cx={d.pt.x} cy={d.pt.y} r={r + 2} fill={tone === "cream" ? "#FAFAF8" : "#0A0A0A"} />
                <circle cx={d.pt.x} cy={d.pt.y} r={r} fill={c} />
                {showLabel && (
                  <text
                    x={d.pt.x + r + 4} y={d.pt.y + 3}
                    fontFamily="DM Sans, sans-serif"
                    fontSize={isActive ? "12" : "10"}
                    fontWeight={isActive ? "700" : "500"}
                    fill={textFill}
                  >
                    {d.city}
                    {isActive && (
                      <tspan fontFamily="DM Mono, monospace" fontSize="10" fill={c} fontWeight="700">
                        {" "}· ${d.total.toLocaleString()}
                      </tspan>
                    )}
                  </text>
                )}
              </g>
            );
          })}
        </g>

        {/* Corner crosshairs */}
        <g fill="none" stroke={bgDots} strokeWidth="1">
          <path d={`M 10 10 L 10 24 M 10 10 L 24 10`} />
          <path d={`M ${W - 10} 10 L ${W - 10} 24 M ${W - 10} 10 L ${W - 24} 10`} />
          <path d={`M 10 ${H - 10} L 10 ${H - 24} M 10 ${H - 10} L 24 ${H - 10}`} />
          <path d={`M ${W - 10} ${H - 10} L ${W - 10} ${H - 24} M ${W - 10} ${H - 10} L ${W - 24} ${H - 10}`} />
        </g>
      </svg>
    </div>
  );
}
